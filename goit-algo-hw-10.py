x_rand = np.random.uniform(a, b, N)  # –í–∏–ø–∞–¥–∫–æ–≤—ñ –∑–Ω–∞—á–µ–Ω–Ω—è x ‚àà [a, b] —É –∫—ñ–ª—å–∫–æ—Å—Ç—ñ N –∑–Ω–∞—á–µ–Ω—å (<class 'numpy.ndarray'>)
