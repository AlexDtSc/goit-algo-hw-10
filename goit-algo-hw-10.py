## –∞–ª–µ –≤–∂–µ –ö—ñ–ª—å–∫—ñ—Å—Ç—å –µ—Å–ø–µ—Ä–∏–º–µ–Ω—Ç—ñ–≤ –∑–Ω–∞—á–Ω–∞ –ø–æ—Ä—ñ–≤–Ω—è–Ω–æ –∑ –í–∞—Ä—ñ–∞–Ω—Ç–æ–º 1 -> num_experiments = 100, —â–æ –ø–æ–∫—Ä–∞—â—É—î —Ç–æ—á–Ω—ñ—Å—Ç—å –ø—Ä–∏–±–ª–∏–∑–Ω–æ–≥–æ –æ–±—á–∏—Å–ª–µ–Ω–Ω—è —ñ–Ω—Ç–µ–≥—Ä–∞–ª–∞ –ø–æ –∞–ª–≥–æ—Ä–∏—Ç–º—É –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ

import random
import numpy as np
import scipy.integrate as spi

# –í–∏–∑–Ω–∞—á–µ–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó, —è–∫—É –ø–æ—Ç—Ä—ñ–±–Ω–æ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞—Ç–∏, –Ω–∞–ø—Ä–∏–∫–ª–∞–¥, f(x) = x^2
def f(x):
    return x ** 2

# –ú–µ–∂—ñ —ñ–Ω—Ç–µ–≥—Ä—É–≤–∞–Ω–Ω—è
a = 0
b = 2

# –ö—ñ–ª—å–∫—ñ—Å—Ç—å –µ–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç—ñ–≤ (—Å–µ—Ä–µ–¥–Ω—î –∑–∞ –∫—ñ–ª—å–∫–∞ –∑–∞–ø—É—Å–∫—ñ–≤)
num_experiments = 100           # –ö—ñ–ª—å–∫—ñ—Å—Ç—å –µ–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç—ñ–≤
points_per_experiment = 100_000 # –∫—ñ–ª—å–∫—ñ—Å—Ç—å —Ç–æ—á–æ–∫ –¥–ª—è –∫–æ–∂–Ω–æ–≥–æ –µ–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç—É

def monte_carlo_integral(f, a, b, num_experiments, points_per_experiment):
    total = 0
    for _ in range(num_experiments):
        # –ì–µ–Ω–µ—Ä—É—î–º–æ –≤–∏–ø–∞–¥–∫–æ–≤—ñ —Ç–æ—á–∫–∏ x –∑ —ñ–Ω—Ç–µ—Ä–≤–∞–ª—É [a, b]
        xs = [random.uniform(a, b) for _ in range(points_per_experiment)]
        # –û–±—á–∏—Å–ª—é—î–º–æ –∑–Ω–∞—á–µ–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó –≤ —Ü–∏—Ö —Ç–æ—á–∫–∞—Ö
        values_y = [f(x) for x in xs]
        # –ü–ª–æ—â–∞ –ø—Ä—è–º–æ–∫—É—Ç–Ω–∏–∫–∞: —à–∏—Ä–∏–Ω–∞ —ñ–Ω—Ç–µ—Ä–≤–∞–ª—É √ó —Å–µ—Ä–µ–¥–Ω—î –∑–Ω–∞—á–µ–Ω–Ω—è —Ñ—É–Ω–∫—Ü—ñ—ó (–≤–∏—Å–æ—Ç–∏ —Ñ—ñ–≥—É—Ä–∏)
        area = (b - a) * (sum(values_y) / len(values_y))
        total += area
    return total / num_experiments

# –û–±—á–∏—Å–ª–µ–Ω–Ω—è —ñ–Ω—Ç–µ–≥—Ä–∞–ª—É –º–µ—Ç–æ–¥–æ–º –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ
approx_integral = monte_carlo_integral(f, a, b, num_experiments, points_per_experiment)

# –ê–Ω–∞–ª—ñ—Ç–∏—á–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è –ø–æ—Ä—ñ–≤–Ω—è–Ω–Ω—è
exact_integral, error = spi.quad(f, a, b)

# –í–∏–≤–µ–¥–µ–Ω–Ω—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤
print('=== –í–∞—Ä—ñ–∞–Ω—Ç2 ====\n')

print(f"  –Ü–Ω—Ç–µ–≥—Ä–∞–ª –º–µ—Ç–æ–¥–æ–º –ú–æ–Ω—Ç–µ-–ö–∞—Ä–ª–æ (–∫—ñ–ª—å–∫—ñ—Å—Ç—å –µ–∫—Å–ø–µ—Ä–∏–º–µ–Ω—Ç—ñ–≤ = {num_experiments}):")
